package com.captaindroid.lineanimation.utils;

public class Coordinates {
    int x, y;
    float xf, yf;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public float getXf(){
        return xf;
    }

    public void setXf(float xf){
        this.xf = xf;
    }

    public float getYf(){
        return yf;
    }

    public void setYf(float yf){
        this.yf = yf;
    }
}
